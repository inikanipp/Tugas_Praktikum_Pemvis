    private void setDetail(){
        try {
            String sql = "SELECT makanan.id_makanan, makanan.gambar, makanan.nama_makanan,makanan.langkah_langkah, makanan.total_harga_bahan, makanan.bahan_utama,makanan.deskripsi ,makanan.sejarah, wilayah.wilayah AS nama_wilayah FROM makanan JOIN wilayah ON  makanan.id_wilayah = wilayah.id_wilayah WHERE id_makanan = ?;";
            Connection conn = koneksi.getKoneksi(); // Pastikan metode koneksi.getKoneksi() mengembalikan objek Connection yang valid
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, idMakanan);

            java.sql.ResultSet res = ps.executeQuery();
            
            if (res.next()){
                    jTextFieldNamaKulinerDetail.setText(res.getString("nama_makanan"));
                    jTextFieldAsalDetail.setText(res.getString("nama_wilayah"));
                    jTextFieldTotalHargaBahanDetail.setText(res.getString("total_harga_bahan"));
                    jTextFieldBahanUtamaDetail.setText(res.getString("bahan_utama"));
                    jTextAreaLangkahDetail.setText(res.getString("langkah_langkah"));
                    jTextAreajLabelSejarah1Detail.setText(res.getString("sejarah"));
                    jTextAreaDeskripsi2Detail.setText(res.getString("deskripsi"));
                    
                    byte[] imgBytes = res.getBytes("gambar");

                    // Konversi byte[] ke ImageIcon
                    ImageIcon icon = new ImageIcon(imgBytes);

                    // Ubah ukuran gambar (misal: 200x200)
                    Image scaledImage = icon.getImage().getScaledInstance(313, 313, Image.SCALE_SMOOTH);
                    jLabelGambarDetail.setIcon(new ImageIcon(scaledImage));
            
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
            
    }
